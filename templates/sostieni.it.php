                <h1>Perché il crowdfunding a teatro</h1>
                <p class="justify">
                    Una volta approvato, il progetto può accedere alla raccolta fondi tramite la nostra piattaforma di crowdfunding. In questo modo, non solo l'artista potrà ottenere i fondi di cui necessita per realizzare la propria opera, ma ogni spettatore si sentirà responsabilizzato a dare il proprio contributo tramite una libera donazione.
                    Questo vuol dire che chiunque potrà fruire delle opere in cartellone gratuitamente, ma significa anche che, se si sarà appassionato abbastanza a una di esse, allora farà sicuramente la sua offerta libera, garantendone così la sua completa realizzazione.
                    Inoltre, il crowdfunding è il modo perfetto per mantenere quella pro-attività che il pubblico teatrale ha per sua stessa natura: infatti, tramite i propri finanziamenti, sarà il pubblico stesso, e non il nostro staff di art directors, a stabilire quale opera inserire nella stagione del nostro teatro e dunque a decidere cosa vedere. E tutto questo contribuirà a rendere il nostro teatro un luogo libero e democratico!
                </p>
