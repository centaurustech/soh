                <h1>Why Crowdfunding for Theatre</h1>
                <p class="justify">
                    Once approved, the project can access to the fundraising through our crowdfunding platform. This way not only the artist will be able to obtain the necessary funds to make his/her own opera, but also each viewer will feel responsible to give a contribution through a free donation.
                    This means that anyone can access the opera programme freely, but it also implies that if you get passionate enough about one of them, you will surely make your offer, ensuring that the opera will be completed.
                    Additionally, crowdfunding is the perfect why to maintain that kind of proactivity that the theatrical audience naturally has: in fact, through its funding, the public itself, and not our staff of Art Directors, will establish what opera should be inserted in the season of our theatre and thus choose what to see. And this will contribute to make our theatre a free and democratic space!
